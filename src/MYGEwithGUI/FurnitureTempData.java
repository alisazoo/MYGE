package MYGEwithGUI;

public class FurnitureTempData extends FurnitureDialog{

    private String furnitureName;
    private int furnitureWidth, furnitureLength;

    public String getFurnitureName() {
        return furnitureName;
    }

    public void setFurnitureName(String furnitureName) {
        this.furnitureName = furnitureName;
    }

    public int getFurnitureWidth() {
        return furnitureWidth;
    }

    public void setFurnitureWidth(int furnitureWidth) {
        this.furnitureWidth = furnitureWidth;
    }

    public int getFurnitureLength() {
        return furnitureLength;
    }

    public void setFurnitureLength(int furnitureLength) {
        this.furnitureLength = furnitureLength;
    }
}
